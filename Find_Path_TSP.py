import numpy as np
import Final.A_Star as at

current_length = 0
path = [0] * 11
bestpath = [0] * 11
all_length = np.inf


def nonaccess(index):
    i = 0
    while i < index:
        if path[i] == path[index]:
            return False
        i += 1
    return True


def Find_path_TSP(WeightMap, n, index):
    global all_length
    global current_length
    if (index == n - 1):
        for i in range(n):
            path[index] = i
            if (nonaccess(index) and current_length + WeightMap[path[index - 1]][path[index]] + WeightMap[path[index]][
                path[0]] < all_length):
                all_length = current_length + WeightMap[path[index - 1]][path[index]] + WeightMap[path[index]][path[0]]
                for i in range(n):
                    bestpath[i] = path[i]
    else:
        for i in range(n):
            path[index] = i
            if (nonaccess(index) and current_length + WeightMap[path[index - 1]][path[index]] < all_length):
                current_length += WeightMap[path[index - 1]][path[index]]
                Find_path_TSP(WeightMap, n, index + 1)
                current_length -= WeightMap[path[index - 1]][path[index]]


